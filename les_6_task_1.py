# lesson_1_task_6 "Пользователь вводит номер буквы в алфавите.
# Определить, какая это буква."

from sys import getsizeof

num = int(input('Введите порядковый номер буквы: '))
num += ord('a') - 1
a = chr(num)
total = getsizeof(num) + getsizeof(a)
print(f'Ваша буква: {a}')
print('Итого {} байта '.format(total))
# Введите порядковый номер буквы: 3
# Ваша буква: c
# Итого 78 байта

